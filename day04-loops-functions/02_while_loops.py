# While Loop

# while (condition): # The block keeps executing until the condition is true
    # Body of the loop

# In while loops, the condition s checked first. If it evaluated true, the body of loop executed otherwise not!


# Syntax:

i = 1

while(i<6):
    print(i)
    i+=1

